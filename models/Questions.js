import mongoose from "mongoose";

// Creating a question Schema
const questionSchema = new mongoose.Schema({
  title: String,
  body: String,
  tags: [],
  created_at: {
    type: Date,
    default: Date.now(),
  },
  user: Object,
  comment_id: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Comments",
  },
  viewCount: Number,
});

export default mongoose.model("Questions", questionSchema);